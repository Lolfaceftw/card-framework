    def evaluate_g_eval(self, transcript: str, summary: str) -> Dict[str, float]:
        """
        LLM-as-a-Judge (G-Eval) to score Faithfulness, Coherence, and Relevance.
        Returns scores 1-5.
        """
        # Truncate transcript to fit context window if necessary
        trunc_transcript = transcript[:15000]

        prompt = f"""
        You are an expert evaluator for podcast summarization.

        Source Transcript:
        {trunc_transcript}...

        Generated Summary:
        {summary}

        Evaluate the Generated Summary on the following criteria (Score 1-5):

        1. Faithfulness: Is the information in the summary factually supported by the transcript? (1=Hallucinated, 5=Fully supported)
        2. Coherence: Is the summary well-structured and logical? (1=Incoherent, 5=Perfect flow)
        3. Relevance: Does the summary capture the most important information? (1=Irrelevant, 5=Captures all key points)

        Return ONLY a JSON object: {{"faithfulness": int, "coherence": int, "relevance": int}}
        """

        try:
            response = CLIENT.chat.completions.create(
                model=MODEL_NAME,
                messages=[{"role": "user", "content": prompt}],
                temperature=0,
                response_format={"type": "json_object"}
            )
            return json.loads(response.choices[0].message.content)
        except Exception as e:
            print(f"G-Eval Error: {e}")
            return {"faithfulness": 0, "coherence": 0, "relevance": 0}

    def run_benchmark(self, dataset_path: str, output_csv: str, limit: int = None):
        """
        dataset_path: Path to the TREC json file (list of dicts)
        """
        with open(dataset_path, 'r') as f:
            data = json.load(f)

        if limit:
            data = data[:limit]

        results = []

        print(f"Starting benchmark on {len(data)} episodes...")

        for entry in tqdm(data):
            episode_id = entry.get('episode_id', 'unknown')
            transcript = entry.get('transcript', '')
            human_summary = entry.get('summary', '') # The Ground Truth

            if not transcript:
                continue

            # 1. Generate Candidate Summary (Your System)
            candidate_summary = self.generate_summary(transcript)

            # 2. Compute Reference-Based Metrics (ROUGE & BERTScore)
            rouge_scores = self.rouge.compute(
                predictions=[candidate_summary],
                references=[human_summary]
            )

            bert_scores = self.bertscore.compute(
                predictions=[candidate_summary],
                references=[human_summary],
                lang="en",
                device=self.device
            )

            # 3. Compute Reference-Free Metrics (G-Eval)
            g_eval_scores = self.evaluate_g_eval(transcript, candidate_summary)

            # 4. Compute Pipeline Constraints (Length)
            # Assuming standard podcast speaking rate ~150wpm
            word_count = len(candidate_summary.split())
            est_duration_min = word_count / 150.0

            # Store Results
            row = {
                "episode_id": episode_id,
                "rouge1": rouge_scores['rouge1'],
                "rougeL": rouge_scores['rougeL'],
                "bertscore_f1": bert_scores['f1'][0],
                "faithfulness": g_eval_scores['faithfulness'],
                "coherence": g_eval_scores['coherence'],
                "relevance": g_eval_scores['relevance'],
                "word_count": word_count,
                "est_duration_min": est_duration_min,
                "generated_text": candidate_summary,
                "ground_truth_text": human_summary
            }
            results.append(row)

        # Save to CSV
        df = pd.DataFrame(results)
        df.to_csv(output_csv, index=False)

        print("\n--- Benchmark Results ---")
        print(f"Avg ROUGE-L: {df['rougeL'].mean():.4f}")
        print(f"Avg BERTScore F1: {df['bertscore_f1'].mean():.4f}")
        print(f"Avg Faithfulness (1-5): {df['faithfulness'].mean():.2f}")
        print(f"Avg Relevance (1-5): {df['relevance'].mean():.2f}")
        print(f"Results saved to {output_csv}")

